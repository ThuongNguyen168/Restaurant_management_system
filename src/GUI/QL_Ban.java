    public void Print() {
        Object[] rows;
        try {
            BanDAO pbd = new BanDAO();
            ArrayList<Ban> pb = new ArrayList<Ban>();
            pb = pbd.layBAN();
            for (Ban b : pb) {
                rows = new Object[]{b.getMaban(), b.getSoghe(),b.getGhichu()};
                tableModel.addRow(rows);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

