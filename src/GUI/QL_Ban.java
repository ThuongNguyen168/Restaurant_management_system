    private void btn_them2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_them2ActionPerformed
        Ban p = new Ban();
        p.setSoghe(Integer.valueOf(txt_soghe.getText()));
        BanDAO pbd = new BanDAO();
        boolean check = pbd.themban(p);
        if(check == true)
        {
            JOptionPane.showMessageDialog(this, "Thêm Thành Công");
            tableModel.setRowCount(0);
            Print();
        }
            
        else
            JOptionPane.showMessageDialog(this, "Thêm Không Thành Công");
    }//GEN-LAST:event_btn_them2ActionPerformed

    private void tbl_banMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_banMouseClicked
        int row = this.tbl_ban.getSelectedRow();
        int ma = (int)tbl_ban.getValueAt(row, 0);
        String s = Integer.toString(ma);
        this.txt_maban.setText(s);
        int ma2 = (int)tbl_ban.getValueAt(row, 1);
        String s2 = Integer.toString(ma2);
        this.txt_maban.setText(s);
        this.txt_soghe.setText(s2);
    }//GEN-LAST:event_tbl_banMouseClicked

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        if(txt_maban.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Muốn Xóa Phải Chọn 1 Đứa");
            
        }
        else
        {
            if (JOptionPane.showConfirmDialog(this, "Xóa Phòng " + txt_maban.getText() + " Chứ ?") == JOptionPane.OK_OPTION)
            {
                BanDAO pbd = new BanDAO();
                boolean check = pbd.xoaban(Integer.parseInt(txt_maban.getText()));
                if(check == true)
                {
                    JOptionPane.showMessageDialog(this, "Xóa Thành Công");
                    tableModel.setRowCount(0);
                    Print();
                }

                else
                    JOptionPane.showMessageDialog(this, "Xóa Không Thành Công");
            }   
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

