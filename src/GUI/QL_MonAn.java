    private void hienThiDsMon() {
        ArrayList<MonAn> dsMA = MonAnDAO.DsMonAn();
        tableMonAn.setRowCount(0);
        for( MonAn ma : dsMA)
        {
            Vector<Object> rowData = new Vector<Object>();
            rowData.add(ma.getMamon());
            rowData.add(ma.getTenmon());
            rowData.add(ma.getGia());
            rowData.add(ma.getTennguyenlieu());
            rowData.add(ma.getTenLoai());
            tableMonAn.addRow(rowData);
           
           
        }
        table_DSMonAn.setModel(tableMonAn);
    }
    
    
