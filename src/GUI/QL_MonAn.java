    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        MonAn monAn = new MonAn();
        if(txtTenMon.getText().equals("") || txtDonGia.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Đầy Đủ Thông Tin");
        }
        else
        {
            if(MonAnDAO.ktrMonAn(txtTenMon.getText()) == true)
            {
                monAn.setTenmon(txtTenMon.getText());
                monAn.setGia(Integer.parseInt(txtDonGia.getText()));

                monAn.setMaloai(MonAnDAO.layMaLoaiTheoTen(cBLoaiMon.getSelectedItem().toString()));
                monAn.setManuyenlieu(MonAnDAO.layMaNguyenLieuTheoTen(cBNguyenLieu.getSelectedItem().toString()));

                boolean  check = MonAnDAO.themMonAn(monAn);
                if(check == true)
                {
                    JOptionPane.showMessageDialog(this, "Thêm Món Ăn Thành Công !!");
                    btnLuu.setEnabled(false);
                    setEnaBled(true);
                    hienThiDsMon();
                     txtTenMon.setText("");
                    txtDonGia.setText("");
                    txtMaMon.setText("");
                   
                    setEnaBled(false);
                     btnThem.setEnabled(true);
                
                    

                }
                else
                {
                     JOptionPane.showMessageDialog(this, "Thêm Món Ăn Thất Bại !!");
                }
                
            }
            else{
                JOptionPane.showMessageDialog(this, "Món Ăn Đã Có !!");
               
            }
            
            
        }
        
        
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        MonAn monAn = new MonAn();
        if(txtTenMon.getText().equals("") || txtDonGia.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Vui Lòng Nhập Đầy Đủ Thông Tin");
        }
        else
        {
            monAn.setMamon(Integer.parseInt(txtMaMon.getText()));
                monAn.setTenmon(txtTenMon.getText());
                monAn.setGia(Integer.parseInt(txtDonGia.getText()));

                monAn.setMaloai(MonAnDAO.layMaLoaiTheoTen(cBLoaiMon.getSelectedItem().toString()));
                monAn.setManuyenlieu(MonAnDAO.layMaNguyenLieuTheoTen(cBNguyenLieu.getSelectedItem().toString()));

                boolean  check = MonAnDAO.SuaMonAn(monAn);
                if(check == true)
                {
                    JOptionPane.showMessageDialog(this, "Thêm Món Ăn Thành Công !!");
                    btnLuu.setEnabled(false);
                    setEnaBled(false);
                    hienThiDsMon();
                    txtTenMon.setText("");
                    txtDonGia.setText("");
                    txtMaMon.setText("");
                    btnThem.setEnabled(true);
                    

                }
                else
                {
                     JOptionPane.showMessageDialog(this, "Sữa Món Ăn Thất Bại !!");
                }
            
            
        }
        
        
        
    }//GEN-LAST:event_btnSuaActionPerformed

